-- Crear tabla de Alumnos
CREATE TABLE IF NOT EXISTS USUARIOS (
    ID_USR VARCHAR(100) PRIMARY KEY,  
    ROLEE VARCHAR(2),
    PASS VARCHAR(100),
    MATRICULA VARCHAR(100)

